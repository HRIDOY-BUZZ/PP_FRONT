        <p><strong>TYPES OF COOKIE</strong></p>
        <p>There are four main types of cookie generally used on websites:</p>
        <p><strong>Strictly necessary cookies:</strong> These cookies are essential in order to enable you to move
            around the website and use its features, such as accessing secure areas of the website. Without these
            cookies services you have asked for (like selecting a product for purchase) cannot be provided.</p>
        <p><strong>Functionality cookies:</strong> These cookies allow the website to remember choices you make (such as
            your user name, language or the region you are in) and provide enhanced features. For instance, they may be
            used to remember your account details and personal preferences, or provide services you have asked for such
            as rating a review or sharing via social media. The information these cookies collect may be anonymised and
            they cannot track your browsing activity on other websites.</p>
        <p><strong>Performance cookies:</strong> These cookies collect information about how visitors use a website, for
            instance which pages visitors go to most often, and if they get error messages from web pages. These cookies
            don&rsquo;t collect information that identifies a visitor. All information these cookies collect is
            aggregated and therefore anonymous. It is only used to improve how a website works.</p>
        <p><strong>Ad targeting cookies:</strong> These cookies are used to deliver adverts more relevant to you and
            your interests. They are also used to limit the number of times you see an advertisement as well as help
            measure the effectiveness of the advertising campaign. They remember that you have visited a website and
            this information is shared with other organisations such as advertisers.</p>
